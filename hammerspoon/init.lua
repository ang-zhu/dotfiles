    local win = hs.window.focusedWindow()
    local f = win:frame()
    local screen = win:screen()
    local max = screen:frame()

    f.w = max.w / 5
    f.h = max.h / 4
    f.x = max.w / 2 - f.w / 2
    f.y = max.h / 2 - f.h / 2
    
    win:setFrame(f)
end)


-----------------------------------------------
-- switch window between screens
-----------------------------------------------

hs.hotkey.bind(hyper, "w", grid.pushWindowNextScreen)

